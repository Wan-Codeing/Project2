        function loadHTMLFile(index) {
            return htmlFiles[index];
        }
        let currentFileIndex = 3;
        function handleWheel(event) {
            const page04 = document.getElementById("page04");
            page04.style.display='none';
            if (event.deltaY > 0) {
                currentFileIndex++;
                if (currentFileIndex >= 7) {
                    currentFileIndex = 0;
                }
            } else {
                currentFileIndex--;
                if (currentFileIndex < 0) {
                    currentFileIndex = 6;
                }
            }
            showNextImage();
            setInterval(showNextImage, 1900);
            loadHTMLFile(currentFileIndex);
            setTimeout(function() {
                location.href=(loadHTMLFile(currentFileIndex));
            },1900);
        }
        document.addEventListener('wheel', handleWheel)

        function showNextImage() {
            currentIndex = (currentIndex+1) % images.length;
            images[currentIndex].style.display = 'block';
            images[currentIndex].style.opacity = '1';
        }